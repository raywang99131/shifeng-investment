export const STOCK_OPTIONS = STOCK_LIST.map((s) => ({
  value: s.code,
  label: `${s.code} ${s.name}`,
  name: s.name,
  code: s.code,
}));

export function searchStocks(keyword: string) {
  if (!keyword) return STOCK_OPTIONS;
  const kw = keyword.toUpperCase();
  return STOCK_OPTIONS.filter(
    (s) => s.code.includes(kw) || s.name.includes(keyword)
  );
}
